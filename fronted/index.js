function loadPhones(){
    document.title = phones[koji].model;
    $('#naziv')[0].innerHTML = phones[koji].model;
    $('#slika')[0].src=phones[koji].phoneImage;
    
    var tabela = $('#tabela')[0];
    var lista = ['Launch','Platform','Memory','Batery','Camera','Screen','Other'];
    var j=0;
    for(i=0;i<lista.length;i++)
    {
        var row = tabela.insertRow();
        
        var cell = row.insertCell();
        var cell1 = row.insertCell();
        cell.innerHTML = phones[koji].characteristics[j];
        j++;
        cell1.innerHTML = phones[koji].characteristics[j];
        j++;
    }
    $('#slika1')[0].src=phones[koji].image1;
    $('#slika2')[0].src=phones[koji].image2;
    $('#slika3')[0].src=phones[koji].image3;
    $('#name_review')[0].innerHTML = phones[koji].name_review;
    $('#author')[0].innerHTML = phones[koji].author;
    $('#review')[0].innerHTML = phones[koji].review;
    $('#video')[0].src = phones[koji].video;
}

